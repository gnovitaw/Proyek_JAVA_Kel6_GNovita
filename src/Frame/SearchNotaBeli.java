     public ArrayList<NotaBeliSearch> List(String ValToSearch)
    {
        ArrayList<NotaBeliSearch> nbsearch = new ArrayList<NotaBeliSearch>();
        Statement st;
        ResultSet rs;
        try{
            Connection con = getConnection();
            st=con.createStatement();
             String query="SELECT * FROM `nota beli` WHERE  CONCAT(`bno`,`sname`,`product`,`amt`,`pdate`) LIKE '%"+ValToSearch+"%';";
            rs=st.executeQuery(query);
            
            NotaBeliSearch notbelsearch;
            
            while(rs.next())
            {
               notbelsearch = new NotaBeliSearch(rs.getInt("bno"), rs.getString("sname"),rs.getString("product"),rs.getInt("amt"),rs.getInt("qty"),rs.getString("pdate")
               ); 
               nbsearch.add(notbelsearch);
            }
            
            
        }
        catch(Exception e)
        {
            System.out.println(e.getMessage());
        }
        return nbsearch;
    
    }
    
    public void findbill()
        {
            ArrayList <NotaBeliSearch> bills = List(jTextField1.getText());
            DefaultTableModel model = new DefaultTableModel();
            model.setColumnIdentifiers(new Object[]{"Billno","Supplier Name","Product","Amount","Qty","Date"});
            Object[] row =new Object [6] ;
            for(int i=0;i < bills.size(); i++)
            {
                row[0] = bills.get(i).getbno();
                row[1] = bills.get(i).getsname();
                row[2] = bills.get(i).getproduct();
                row[3] = bills.get(i).getamt();
                row[4] = bills.get(i).getqty();
                row[5] = bills.get(i).pdate();
                model.addRow(row);
            }
            jTable1.setModel(model);
            
        }
